/*
 * 키워드(keyword:예약어) : 언어에서 단어에 의미를 부여해서 정의해 놓은 것.
 */

public class Java001_main {
public static void main(String[] args){
	System.out.println(5); //정수숫자
	System.out.println('a'); //문자(char)
	System.out.println("java"); //문자열(string)
	System.out.println(5.0); //실수 숫자
	System.out.println(true); //논리
}
}
